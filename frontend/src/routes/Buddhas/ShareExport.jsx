/**
 * ShareExport — copy the current session summary to the clipboard as text.
 *
 * Builds a small plain-text summary from the live `buddhaStatus` payload and
 * writes it to navigator.clipboard, with graceful fallback to a hidden
 * textarea + document.execCommand('copy') on browsers without the async
 * Clipboard API. Shows a brief AntD toast on success / failure.
 *
 * @component
 * @param {Object} props
 * @param {Object} [props.buddhaStatus] The live recitation status from useWebSocketStable.
 * @param {string} [props.intention]    Current intention text to include in the export.
 */
import React, { useCallback } from 'react';
import { Card, Button, Typography, Space, message } from 'antd';
import { Share2, ClipboardCopy, Check } from 'lucide-react';

const { Text, Title } = Typography;

function buildSummary({ buddhaStatus, intention }) {
  const lines = ['88 Buddhas — Recitation Summary', '─'.repeat(32)];
  if (intention) lines.push(`Intention: ${intention}`);
  if (buddhaStatus) {
    lines.push(`Cycle: ${buddhaStatus.current_cycle || 0}`);
    lines.push(`Total recited: ${buddhaStatus.total_recited || 0}`);
    lines.push(`Mala count: ${buddhaStatus.mala_count || 0}`);
    lines.push(`Dedications: ${buddhaStatus.dedications || 0}`);
    const cb = buddhaStatus.current_buddha;
    if (cb && cb.name_chinese) {
      lines.push(
        `Current: ${cb.name_chinese}` +
        (cb.name_pinyin ? ` (${cb.name_pinyin})` : ''),
      );
    }
    if (buddhaStatus.progress_pct != null) {
      lines.push(`Progress: ${Math.round(buddhaStatus.progress_pct)}%`);
    }
  } else {
    lines.push('(No active session — connect via WebSocket to capture live data.)');
  }
  lines.push('─'.repeat(32));
  lines.push('Generated by Vajra.Stream');
  return lines.join('\n');
}

async function copyToClipboard(text) {
  // Preferred async Clipboard API (secure contexts only).
  if (navigator.clipboard && window.isSecureContext) {
    await navigator.clipboard.writeText(text);
    return;
  }
  // Legacy fallback: hidden textarea + execCommand.
  const ta = document.createElement('textarea');
  ta.value = text;
  ta.setAttribute('readonly', '');
  ta.style.position = 'absolute';
  ta.style.left = '-9999px';
  document.body.appendChild(ta);
  ta.select();
  try {
    const ok = document.execCommand('copy');
    document.body.removeChild(ta);
    if (!ok) throw new Error('execCommand returned false');
  } catch (err) {
    document.body.removeChild(ta);
    throw err;
  }
}

export default function ShareExport({ buddhaStatus, intention }) {
  const handleCopy = useCallback(async () => {
    const text = buildSummary({ buddhaStatus, intention });
    try {
      await copyToClipboard(text);
      message.success('Summary copied to clipboard.');
    } catch (err) {
      console.error('ShareExport: copy failed', err);
      message.error('Could not copy — clipboard unavailable.');
    }
  }, [buddhaStatus, intention]);

  const disabled = !buddhaStatus && !intention;

  return (
    <Card
      size="small"
      title={
        <Space size={8} align="center">
          <Share2 size={16} className="text-blue-400" />
          <Title level={5} style={{ margin: 0 }}>Share / Export</Title>
        </Space>
      }
    >
      <Space direction="vertical" size={8} style={{ width: '100%' }}>
        <Text type="secondary" style={{ fontSize: 12 }}>
          Copy a plain-text snapshot of this recitation session to share anywhere.
        </Text>
        <Space size={8} wrap>
          <Button
            type="primary"
            icon={<ClipboardCopy size={14} />}
            onClick={handleCopy}
            disabled={disabled}
          >
            Copy as text
          </Button>
          <Text type="secondary" italic style={{ fontSize: 11 }}>
            <Check size={10} style={{ verticalAlign: 'middle', marginRight: 4 }} />
            No tracking — text stays on your device.
          </Text>
        </Space>
      </Space>
    </Card>
  );
}
